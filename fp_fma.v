// =============================================================
// File: fp_fma.v
// Chức năng: Fused Multiply-Add IEEE 754 single-precision
//            result = A * B + C
//
// Pipeline 3 Stage:
//   S1 : Unpack (combinational) → pipeline reg
//   S2 : mantissa_multiplier (registered) + Align/Add (comb)
//   S3 : Normalize + Pack → registered output
//
// Latency   : 3 chu kỳ
// Throughput: 1 kết quả / chu kỳ
// =============================================================

`timescale 1ns/1ps

module fp_fma (
    input         clk,
    input  [31:0] floatA,
    input  [31:0] floatB,
    input  [31:0] floatC,
    output reg [31:0] result
);

    // =========================================================
    // S1 — UNPACK (combinational)
    // =========================================================
    wire        sA = floatA[31];
    wire        sB = floatB[31];
    wire        sC = floatC[31];
    wire [7:0]  eA = floatA[30:23];
    wire [7:0]  eB = floatB[30:23];
    wire [7:0]  eC = floatC[30:23];

    wire [23:0] mA = (eA == 8'd0) ? {1'b0, floatA[22:0]} : {1'b1, floatA[22:0]};
    wire [23:0] mB = (eB == 8'd0) ? {1'b0, floatB[22:0]} : {1'b1, floatB[22:0]};
    wire [23:0] mC = (eC == 8'd0) ? {1'b0, floatC[22:0]} : {1'b1, floatC[22:0]};

    wire        s_prod_comb   = sA ^ sB;
    wire [8:0]  exp_prod_comb = eA + eB - 9'd127;
    wire        is_zero_A     = (eA == 8'd0);
    wire        is_zero_B     = (eB == 8'd0);
    wire        is_zero_C     = (eC == 8'd0);

    // =========================================================
    // S1→S2 Pipeline register
    // =========================================================
    reg [8:0]  exp_prod_r;
    reg        s_prod_r;
    reg [8:0]  eC_r;
    reg [23:0] mC_r;
    reg        sC_r;
    reg        zero_AB_r;
    reg        zero_C_r;

    always @(posedge clk) begin
        exp_prod_r <= exp_prod_comb;
        s_prod_r   <= s_prod_comb;
        eC_r       <= {1'b0, eC};
        mC_r       <= mC;
        sC_r       <= sC;
        zero_AB_r  <= is_zero_A | is_zero_B;
        zero_C_r   <= is_zero_C;
    end

    // Mantissa multiplier: input comb → output registered (+1 cycle)
    wire [47:0] m_prod;
    mantissa_multiplier mul_inst (
        .clk    (clk),
        .A      (mA),
        .B      (mB),
        .Product(m_prod)
    );

    // =========================================================
    // S2 — ALIGN + ADD/SUB (combinational)
    // =========================================================
    wire        norm_prod      = m_prod[47];
    wire [8:0]  exp_prod_adj   = exp_prod_r + {8'b0, norm_prod};
    wire [47:0] prod_mant_norm = norm_prod ? m_prod : (m_prod << 1);
    wire [47:0] prod_mant      = zero_AB_r ? 48'b0 : prod_mant_norm;
    wire [8:0]  prod_exp       = zero_AB_r ? 9'd0  : exp_prod_adj;

    wire [47:0] c_mant_shifted = {mC_r, 24'b0};
    wire signed [9:0] exp_diff =
        $signed({1'b0, prod_exp}) - $signed({1'b0, eC_r});

    wire [47:0] c_mant_aligned =
        (exp_diff >= 10'sd48) ? 48'b0 :
        (exp_diff >= 10'sd0)  ? (c_mant_shifted >> exp_diff[5:0]) :
                                 c_mant_shifted;

    wire [47:0] prod_mant_aligned =
        (exp_diff < 10'sd0 && -exp_diff < 10'sd48) ?
            (prod_mant >> (-exp_diff[5:0])) :
        (exp_diff < 10'sd0) ? 48'b0 : prod_mant;

    wire [8:0] work_exp = (exp_diff < 10'sd0) ? eC_r[8:0] : prod_exp;

    wire eff_sub          = s_prod_r ^ sC_r;
    wire [48:0] add_result    = {1'b0, prod_mant_aligned} + {1'b0, c_mant_aligned};
    wire [48:0] sub_p_minus_c = {1'b0, prod_mant_aligned} - {1'b0, c_mant_aligned};
    wire [48:0] sub_c_minus_p = {1'b0, c_mant_aligned}   - {1'b0, prod_mant_aligned};
    wire        prod_ge_c     = (prod_mant_aligned >= c_mant_aligned);

    wire [48:0] sum_mag  =
        eff_sub ? (prod_ge_c ? sub_p_minus_c : sub_c_minus_p) : add_result;
    wire        sum_sign =
        eff_sub ? (prod_ge_c ? s_prod_r : sC_r) : s_prod_r;

    // Leading-zero count (priority encoder 49-bit)
    reg [5:0] lz;
