            49'b00000000000000000000000000000000000000000001?????: lz = 43;
            49'b000000000000000000000000000000000000000000001????: lz = 44;
            49'b0000000000000000000000000000000000000000000001???: lz = 45;
            49'b00000000000000000000000000000000000000000000001??: lz = 46;
            49'b000000000000000000000000000000000000000000000001?: lz = 47;
            49'b0000000000000000000000000000000000000000000000001: lz = 48;
            default:                                               lz = 63;
        endcase
    end

    // =========================================================
    // S3 — NORMALIZE + PACK (registered output)
    // =========================================================
    wire [48:0] sum_norm  = sum_mag << lz;
    wire signed [9:0] exp_out_s =
        $signed({1'b0, work_exp}) + 10'sd1 - $signed({4'b0, lz});
    wire [8:0]  exp_out   = exp_out_s[8:0];
    wire [22:0] mant_out  = sum_norm[47:25];
    wire        result_zero = (lz == 6'd63) || (zero_C_r && zero_AB_r);

    always @(posedge clk) begin
        if (result_zero)
            result <= {sum_sign, 31'b0};
        else if (exp_out[8] && !exp_out_s[9])
            result <= {sum_sign, 8'hFF, 23'b0};
        else if (exp_out_s[9] || exp_out == 9'b0)
            result <= {sum_sign, 31'b0};
        else
            result <= {sum_sign, exp_out[7:0], mant_out};
    end

endmodule