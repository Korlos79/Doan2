        casez (sum_mag[48:0])
            49'b1????????????????????????????????????????????????: lz = 0;
            49'b01???????????????????????????????????????????????: lz = 1;
            49'b001??????????????????????????????????????????????: lz = 2;
            49'b0001?????????????????????????????????????????????: lz = 3;
            49'b00001????????????????????????????????????????????: lz = 4;
            49'b000001???????????????????????????????????????????: lz = 5;
            49'b0000001??????????????????????????????????????????: lz = 6;
            49'b00000001?????????????????????????????????????????: lz = 7;
            49'b000000001????????????????????????????????????????: lz = 8;
            49'b0000000001???????????????????????????????????????: lz = 9;
            49'b00000000001??????????????????????????????????????: lz = 10;
            49'b000000000001?????????????????????????????????????: lz = 11;
            49'b0000000000001????????????????????????????????????: lz = 12;
            49'b00000000000001???????????????????????????????????: lz = 13;
            49'b000000000000001??????????????????????????????????: lz = 14;
